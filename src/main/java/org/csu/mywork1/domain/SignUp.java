package org.csu.mywork1.domain;

public class SignUp {
    private String signUpTheme;
    private String signUpName;
    private String signUpTelephone;

    public String getSignUpTheme() {
        return signUpTheme;
    }

    public void setSignUpTheme(String signUpTheme) {
        this.signUpTheme = signUpTheme;
    }

    public String getSignUpName() {
        return signUpName;
    }

    public void setSignUpName(String signUpName) {
        this.signUpName = signUpName;
    }

    public String getSignUpTelephone() {
        return signUpTelephone;
    }

    public void setSignUpTelephone(String signUpTelephone) {
        this.signUpTelephone = signUpTelephone;
    }
}
